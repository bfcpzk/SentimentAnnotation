package ruc.lisa.zhaokangpan.sentianno.model;

public class HouseWeibo {

	private String id;
	private String wid;
	private String author;
	private String text;
	private String date;
	private String zan;
	private String zhuanfa;
	private String pinglun;
	private String face;
	private String sent_label;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getWid() {
		return wid;
	}

	public void setWid(String wid) {
		this.wid = wid;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getZan() {
		return zan;
	}

	public void setZan(String zan) {
		this.zan = zan;
	}

	public String getZhuanfa() {
		return zhuanfa;
	}

	public void setZhuanfa(String zhuanfa) {
		this.zhuanfa = zhuanfa;
	}

	public String getPinglun() {
		return pinglun;
	}

	public void setPinglun(String pinglun) {
		this.pinglun = pinglun;
	}

	public String getFace() {
		return face;
	}

	public void setFace(String face) {
		this.face = face;
	}

	public String getSent_label() {
		return sent_label;
	}

	public void setSent_label(String sent_label) {
		this.sent_label = sent_label;
	}

}
